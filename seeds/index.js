const seedUsers = require('./seedUsers');

const seedNotes = require('./seedNotes');

const seedComments = require('./seedComments');

const sequelize = require('../config/connection');

const seedAll = async () => {
  await sequelize.sync({ force: true });
  console.log('------------------------------------------------------------------------------------------------------------------------------------------------');
  await seedUsers();
  console.log('------------------------------------------------------------------------------------------------------------------------------------------------');

  await seedNotes();
  console.log('------------------------------------------------------------------------------------------------------------------------------------------------');

  await seedComments();
  console.log('------------------------------------------------------------------------------------------------------------------------------------------------');

  process.exit(0);
};

seedAll();